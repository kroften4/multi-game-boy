static const char *TAG = "test_rasterizer";

bool bufs_equal(pixel_t *buf1, size_t size1, pixel_t *buf2, size_t size2,
				bool log_all)
{
	if (size1 != size2) {
		ESP_LOGE(TAG, "Different sizes: %zu != %zu", size1, size2);
		return false;
	}

	bool passed = true;
	for (size_t i = 0; i < size1; i++) {
		if (buf1[i] != buf2[i]) {
			ESP_LOGE(TAG, "%#06x != %#06x at %zu", buf1[i], buf2[i], i);
			if (!log_all)
				return false;
			passed = false;
		}
	}
	return passed;
	// return true;
}

